public class Contestents {

    private String contestentImage;
    private String contestentName;
    private String contestentId;



    public String getContestentImage() {
        return contestentImage;
    }

    public String getContestentName() {
        return contestentName;
    }

    public void setContestentImage(String contestentImage) {
        this.contestentImage = contestentImage;
    }
    public void setContestentName(String contestentName) {
        this.contestentName = contestentName ;
    }
    public String getContestentId() {
        return contestentId;
    }

    public void setContestentId(String contestentId) {
        this.contestentId = contestentId;
    }
}
